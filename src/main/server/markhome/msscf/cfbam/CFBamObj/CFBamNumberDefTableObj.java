	public ICFBamNumberDefObj createNumberDef( ICFBamNumberDefObj Obj ) {
		ICFBamNumberDefObj obj = Obj;
		CFBamNumberDefBuff buff = obj.getNumberDefBuff();
		((ICFBamSchema)schema.getBackingStore()).getTableNumberDef().createNumberDef(
			schema.getAuthorization(),
			buff );
		obj.copyBuffToPKey();
		if( obj.getPKey().getClassCode().equals( "a82d" ) ) {
			obj = (ICFBamNumberDefObj)(obj.realise());
		}
		ICFBamValueObj prev = obj.getOptionalLookupPrev();
		if( prev != null ) {
			prev.read( true );
		}
		obj.endEdit();
		return( obj );
	}

