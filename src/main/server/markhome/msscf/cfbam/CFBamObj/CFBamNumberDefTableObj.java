	public ICFBamNumberDefObj readNumberDef( CFBamValuePKey pkey, boolean forceRead ) {
		ICFBamNumberDefObj obj = null;
		if( ( ! forceRead ) && members.containsKey( pkey ) ) {
			obj = members.get( pkey );
		}
		else {
			CFBamNumberDefBuff readBuff = ((ICFBamSchema)schema.getBackingStore()).getTableNumberDef().readDerivedByIdIdx( schema.getAuthorization(),
				pkey.getRequiredTenantId(),
				pkey.getRequiredId() );
			if( readBuff != null ) {
				obj = (ICFBamNumberDefObj)schema.getValueTableObj().constructByClassCode( readBuff.getClassCode() );
				obj.setPKey( ((ICFBamSchema)schema.getBackingStore()).getFactoryValue().newPKey() );
				obj.setBuff( readBuff );
				obj = (ICFBamNumberDefObj)obj.realise();
			}
		}
		return( obj );
	}

