	public ICFBamTimeDefObj createTimeDef( ICFBamTimeDefObj Obj ) {
		ICFBamTimeDefObj obj = Obj;
		CFBamTimeDefBuff buff = obj.getTimeDefBuff();
		((ICFBamSchema)schema.getBackingStore()).getTableTimeDef().createTimeDef(
			schema.getAuthorization(),
			buff );
		obj.copyBuffToPKey();
		if( obj.getPKey().getClassCode().equals( "a843" ) ) {
			obj = (ICFBamTimeDefObj)(obj.realise());
		}
		ICFBamValueObj prev = obj.getOptionalLookupPrev();
		if( prev != null ) {
			prev.read( true );
		}
		obj.endEdit();
		return( obj );
	}

