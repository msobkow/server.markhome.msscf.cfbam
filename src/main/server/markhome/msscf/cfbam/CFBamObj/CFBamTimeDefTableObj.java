	public ICFBamTimeDefObj readTimeDef( CFBamValuePKey pkey, boolean forceRead ) {
		ICFBamTimeDefObj obj = null;
		if( ( ! forceRead ) && members.containsKey( pkey ) ) {
			obj = members.get( pkey );
		}
		else {
			CFBamTimeDefBuff readBuff = ((ICFBamSchema)schema.getBackingStore()).getTableTimeDef().readDerivedByIdIdx( schema.getAuthorization(),
				pkey.getRequiredTenantId(),
				pkey.getRequiredId() );
			if( readBuff != null ) {
				obj = (ICFBamTimeDefObj)schema.getValueTableObj().constructByClassCode( readBuff.getClassCode() );
				obj.setPKey( ((ICFBamSchema)schema.getBackingStore()).getFactoryValue().newPKey() );
				obj.setBuff( readBuff );
				obj = (ICFBamTimeDefObj)obj.realise();
			}
		}
		return( obj );
	}

